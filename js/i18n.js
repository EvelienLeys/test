let LANG = localStorage.getItem('gb_lang') || 'nl';
const t = k => LANGS[LANG]?.[k] ?? LANGS.nl[k] ?? k;

function setLang(l) {
  LANG = l; localStorage.setItem('gb_lang', l);
  // RTL
  const isRtl = LANGS[l]?.rtl === true;
  document.body.classList.toggle('rtl', isRtl);
  document.documentElement.setAttribute('dir', isRtl ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', l);

  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === l));
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t(el.dataset.i18n);
    if (v) el.innerHTML = v.replace(/\n/g,'<br>');
  });
  const cur = document.querySelector('nav a.active');
  render(cur?.dataset.page ?? 'agenda');

  // Stuur taal door naar grondbibliotheek-iframe indien actief
  const grondFrame = document.querySelector('#main iframe[src="grond.html"]');
  if (grondFrame) {
    grondFrame.contentWindow.postMessage({ type: 'setLang', lang: l }, '*');
  }
}

